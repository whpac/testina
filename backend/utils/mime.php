    /**
     * Zwraca typ MIME odpowiadający nazwie pliku (wraz z rozszerzeniem)
     * @param ext Nazwa pliku
     * @param default Typ zwracany, kiedy dane rozszerzenie nie zostanie odnalezione
     */
    static function GetTypeByFileName($name, $default = 'application/octet-stream'){
        $name = strtolower($name);
        if(strpos($name, '.') === false) return $default;
        $ext = substr($name, strrpos($name, '.') + 1);
        return getMimeTypeByExtension($ext, $default);
    }

    /**
     * Zwraca, czy typ MIME jest obsługiwany przez przeglądarkę bez pobierania
     * @param mime Typ MIME
     */
    static function IsSupportedByBrowser($mime){
        $browser_mimes[] = 'text/plain';
        $browser_mimes[] = 'text/html';
        $browser_mimes[] = 'text/css';
        $browser_mimes[] = 'application/xhtml+xml';
        $browser_mimes[] = 'application/xml';
        $browser_mimes[] = 'application/pdf';
        $browser_mimes[] = 'application/javascript';
        return (in_array($mime, $browser_mimes) || strpos($mime, 'image/') !== false);
    }
